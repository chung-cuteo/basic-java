public class Variable {
  public static void main(String[] args) {
    // 8 kieu

    byte bienByte = 10; // 1
    char bienChar = 'A'; // 2
    boolean bienBoolean = false; // 3
    short bienShort = 1; // 4
    int bienInt = 2; // 5
    long bienLong = 2424252; // 6
    float bienFloat = 2.4f; // 7
    double bienDouble = 2.5; // 8

    // hang so
    final int DOISOI = 100;

    System.out.println("byte " + bienByte);
    System.out.println("text " + bienChar);
    System.out.println("boolean " + bienBoolean);
    System.out.println("short " + bienShort);
    System.out.println("int " + bienInt);
    System.out.println("long " + bienLong);
    System.out.println("float " + bienFloat);
    System.out.println("double " + bienDouble);
    System.out.println("final " + DOISOI);
  }
}
